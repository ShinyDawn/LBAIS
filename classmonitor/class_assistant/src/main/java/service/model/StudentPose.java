package service.model;

public class StudentPose {

	private Point nose;
	private Point neck;
	private Point leftShouder;
	private Point rightShouder;
	private Point leftArm;
	private Point rightArm;
	private Point leftWrist;
	private Point rightWrist;

	public Point getNose() {
		return nose;
	}

	public void setNose(Point nose) {
		this.nose = nose;
	}

	public Point getNeck() {
		return neck;
	}

	public void setNeck(Point neck) {
		this.neck = neck;
	}

	public Point getLeftShouder() {
		return leftShouder;
	}

	public void setLeftShouder(Point leftShouder) {
		this.leftShouder = leftShouder;
	}

	public Point getRightShouder() {
		return rightShouder;
	}

	public void setRightShouder(Point rightShouder) {
		this.rightShouder = rightShouder;
	}

	public Point getLeftArm() {
		return leftArm;
	}

	public void setLeftArm(Point leftArm) {
		this.leftArm = leftArm;
	}

	public Point getRightArm() {
		return rightArm;
	}

	public void setRightArm(Point rightArm) {
		this.rightArm = rightArm;
	}

	public Point getLeftWrist() {
		return leftWrist;
	}

	public void setLeftWrist(Point leftWrist) {
		this.leftWrist = leftWrist;
	}

	public Point getRightWrist() {
		return rightWrist;
	}

	public void setRightWrist(Point rightWrist) {
		this.rightWrist = rightWrist;
	}
}
